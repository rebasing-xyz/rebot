package xyz.rebasing.rebot.plugin.urbandictionary.domain;

public class CustomTermResponse {


    private String term;
    private String definition;
    private String example;
    private String permalink;

    public CustomTermResponse(String term, String definition, String permalink) {
        this.term = term;
        this.definition = definition;
        this.permalink = permalink;
    }

    public CustomTermResponse(String term, String definition, String example, String permalink) {
        this.term = term;
        this.definition = definition;
        this.example = example;
        this.permalink = permalink;
    }

    public String getTerm() {
        return term;
    }

    public void setTerm(String term) {
        this.term = term;
    }

    public String getDefinition() {
        return definition;
    }

    public void setDefinition(String definition) {
        this.definition = definition;
    }

    public String getExample() {
        return example;
    }

    public void setExample(String example) {
        this.example = example;
    }

    public String getPermalink() {
        return permalink;
    }

    public void setPermalink(String permalink) {
        this.permalink = permalink;
    }

    @Override
    public String toString() {
        return "CustomTermResponse {" +
                "term='" + term + '\'' +
                ", definition='" + definition + '\'' +
                ", example='" + example + '\'' +
                ", permalink='" + permalink + '\'' +
                '}';
    }
}
