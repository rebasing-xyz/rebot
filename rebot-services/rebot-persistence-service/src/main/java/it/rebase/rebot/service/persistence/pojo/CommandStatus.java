package it.rebase.rebot.service.persistence.pojo;

import it.rebase.rebot.api.object.From;

import javax.persistence.Cacheable;
import javax.persistence.Column;
import javax.persistence.Embedded;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.transaction.Transactional;
import java.time.Instant;

@Entity(name = "COMMAND_STATUS")
@Table(name = "COMMAND_STATUS")
@Cacheable
public class CommandStatus {

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;

    @Column(name = "groupID", nullable = false)
    private Long groupID;

    @Column(name = "commandName", nullable = false)
    private String commandName;

    @Column(name = "isEnabled", nullable = false)
    private boolean isEnabled;

    public CommandStatus(Long groupID, String commandName, boolean isEnabled) {
        this.groupID = groupID;
        this.commandName = commandName;
        this.isEnabled = isEnabled;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getGroupID() {
        return groupID;
    }

    public void setGroupID(Long groupID) {
        this.groupID = groupID;
    }

    public String getCommandName() {
        return commandName;
    }

    public void setCommandName(String commandName) {
        this.commandName = commandName;
    }

    public boolean isEnabled() {
        return isEnabled;
    }

    public void setEnabled(boolean enabled) {
        isEnabled = enabled;
    }

    @Override
    public String toString() {
        return "CommandStatus{" +
                "id=" + id +
                ", groupID=" + groupID +
                ", commandName='" + commandName + '\'' +
                ", isEnabled=" + isEnabled +
                '}';
    }
}
